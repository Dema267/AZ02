import pandas as pd

# Данные об оценках
data = {
    'Ученик': ['Алексей', 'Мария', 'Иван', 'Ольга', 'Дмитрий', 'Елена', 'Сергей', 'Анна', 'Павел', 'Татьяна'],
    'Математика': [5, 4, 4, 4, 3, 3, 5, 5, 4, 3],
    'Физика': [3, 3, 4, 4, 3, 4, 5, 5, 4, 4],
    'Химия': [4, 4, 4, 4, 4, 4, 4, 4, 4, 5],
    'Биология': [5, 5, 5, 5, 5, 5, 5, 4, 3, 5],
    'Литература': [4, 4, 4, 4, 5, 3, 4, 5, 4, 5]
}

# Создание DataFrame
df = pd.DataFrame(data)
df.set_index('Ученик', inplace=True)

# Шаг 2: Вывод первых строк
print("Первые строки DataFrame:")
print(df.head())

# Шаг 3: Средние оценки
mean_scores = df.mean()
print("\nСредние оценки по предметам:")
print(mean_scores)

# Шаг 4: Медианные оценки
median_scores = df.median()
print("\nМедианные оценки по предметам:")
print(median_scores)

# Шаг 5: Q1 и Q3 по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")

# Шаг 6: IQR по математике
IQR_math = Q3_math - Q1_math
print(f"IQR по математике: {IQR_math}")

# Шаг 7: Стандартное отклонение
std_scores = df.std()
print("\nСтандартное отклонение по предметам:")
print(std_scores)
